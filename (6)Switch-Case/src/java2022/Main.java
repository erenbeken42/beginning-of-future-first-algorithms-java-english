package java2022;

public class Main {

	public static void main(String[] args) {
            char grade ='B';
            
            switch(grade) {
            case 'A':
            	System.out.println("Perfect");
            	break;
            case 'B':
            case 'C':
            	System.out.println("Good");
            	break;
            	
            case 'D':
            	System.out.println("Average");
            	break;
            case 'F':
            	System.out.println("Fail");
            	break;
            	default:
            		System.out.println("Invalid Grade");
            	
            		
		
		
		
		
	}
}
}
